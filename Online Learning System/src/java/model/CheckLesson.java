package model;

/**
 *
 * @author sonpk
 */
public class CheckLesson {

    private String type;
    private Object data;

    public CheckLesson() {
    }

    public CheckLesson(String type, Object data) {
        this.type = type;
        this.data = data;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

}
